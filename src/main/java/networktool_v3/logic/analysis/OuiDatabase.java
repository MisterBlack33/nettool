    private static void register(String vendor, String... prefixes) {
        for (String oui : prefixes) {
            OUI.put(oui.toUpperCase(), vendor);
        }
    }

    /**
     * Gibt den Hersteller für einen OUI-Präfix zurück
     * (z.B. "00:1A:8A"), oder {@code null} falls unbekannt.
     */
    public static String lookup(String oui) {
        if (oui == null) return null;
        return OUI.get(oui.toUpperCase());
    }
